const translations = Object.freeze({ en: Object.freeze(en), "zh-CN": Object.freeze(zh) });
let currentLanguage = "en";

export function normalizeLanguage(language) {
  return String(language ?? "").toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function setLanguage(language) {
  currentLanguage = normalizeLanguage(language);
  return currentLanguage;
}

export function getLanguage() {
  return currentLanguage;
}

export function translateFor(language, key, variables = {}) {
  const locale = normalizeLanguage(language);
  const template = translations[locale][key] ?? translations.en[key] ?? key;
  return String(template).replace(/\{([A-Za-z0-9]+)\}/g, (_match, name) => String(variables[name] ?? `{${name}}`));
}

export function t(key, variables = {}) {
  return translateFor(currentLanguage, key, variables);
}

export function translationKeys(language = "en") {
  return Object.keys(translations[normalizeLanguage(language)]).sort();
}
